# Import the math module and use it to:

# Find the square root of 144
# Calculate sin(90°) (hint: use math.radians())

import math

# Find the square root of 144
sqrt_144 = math.sqrt(144)

print(f"The square root of 144 is: {sqrt_144}")

# Calculate sin(90°)
angle_degrees = 90

angle_radians = math.radians(angle_degrees)

sin_90 = math.sin(angle_radians)

print(f"The sine of {angle_degrees} degrees is: {sin_90}")
