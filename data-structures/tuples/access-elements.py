names = ("John", "Jane", "Doe", "Smith")
# + index   0       1       2      3
# - index  -4      -3      -2     -1

# Accessing elements using positive indexing
print(names[0])  # Output: John
print(names[1])  # Output: Jane
print(names[2])  # Output: Doe
print(names[3])  # Output: Smith

# Accessing elements using negative indexing
print(names[-1])  # Output: Smith
print(names[-2])  # Output: Doe
print(names[-3])  # Output: Jane
print(names[-4])  # Output: John
