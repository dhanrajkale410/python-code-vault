a = 7
b = 3

# Arithmetic Operators

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulus:", a % b)
print("Exponentiation:", a ** b)

# Assignment Operators

a += 2  # a = a + 2
print("After a += 2:", a)
b *= 3  # b = b * 3
print("After b *= 3:", b)

# Comparison Operators

print("a == b:", a == b) 
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b)    

# Logical Operators
e = True or False
f = True and False  
g = not True

print("True or False : ", e)
print("True and False:", f)
print("not True:", g)
