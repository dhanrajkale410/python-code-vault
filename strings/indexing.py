#   D     H   A   N   R   A   J
#   0     1   2   3   4   5   6 
#  -1    -2  -3  -4  -5  -6   -7 

name = "DHANRAJ"

print("Using Positive Indexing:")
print(name[0])  # D 
print(name[1])  # H
print(name[2])  # A 
print(name[3])  # N
print(name[4])  # R
print(name[5])  # A
print(name[6])  # J

print("Using Negative Indexing:")
print(name[-1])  # J
print(name[-2])  # A        
print(name[-3])  # R    
print(name[-4])  # N
print(name[-5])  # A
print(name[-6])  # H
print(name[-7])  # D
